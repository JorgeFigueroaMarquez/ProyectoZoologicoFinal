    /**
     * Metodo que llena tabla animales
     */
    private void llenarTablaAnimales(){
        List<Animal> listaAnimales = this.control.recuperarDatos();
        DefaultTableModel modeloTabla = (DefaultTableModel) this.tblAnimales.getModel();
        modeloTabla.setRowCount(0);
        listaAnimales.forEach(animales -> {
            Object[] fila = new Object[4];
            fila[0] = animales.getId();
            fila[1] = animales.getNombre();
            fila[2] = animales.getEdad();
            fila[3] = animales.getSexo();
            modeloTabla.addRow(fila);
        });
    }
    
    /**
     * Metodo que guarda
     */
    private void guarda(){
        Animal animal = new Animal();
        animal.setNombre(this.txtNombreAnimal.getText());
        animal.setEdad(this.txtEdad.getText());
        animal.setSexo(this.txtSexo.getText());
       // control.verificar(animal);
        control.guarda(animal);
        System.out.println("Se guard√≥");
        llenarTablaAnimales();
        limpiarFormulario();
    }
    
    /**
     * Metodo que limpia el formulario
     */
    private void limpiarFormulario(){
        this.txtNombreAnimal.setText(null);
        this.txtSexo.setText(null);
        this.txtEdad.setText(null);
    }
    
    
    
    
    
    
    
    
    
    
    
    
